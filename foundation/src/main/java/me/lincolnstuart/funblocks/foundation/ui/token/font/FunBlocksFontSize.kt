package me.lincolnstuart.funblocks.foundation.ui.token.font

import androidx.compose.ui.unit.TextUnit
import androidx.compose.ui.unit.sp

/**
 * Object that provides font size tokens to standard text components.
 *
 * Based on [androidx.compose.ui.unit.TextUnit].
 */
public object FunBlocksFontSize {
    public val small: TextUnit = 10.sp
    public val medium: TextUnit = 14.sp
    public val large: TextUnit = 16.sp
    public val xLarge: TextUnit = 20.sp
    public val xxLarge: TextUnit = 22.sp
    public val xxxLarge: TextUnit = 24.sp
}
