package me.lincolnstuart.funblocks.foundation.ui.token.font

import androidx.compose.ui.text.font.FontWeight

/**
 * Object that provides font weight tokens to standard text components.
 *
 * Based on [androidx.compose.ui.text.font.FontWeight].
 */
public object FunBlocksFontWeight {
    public val light: FontWeight = FontWeight.W300
    public val regular: FontWeight = FontWeight.W400
    public val semiBold: FontWeight = FontWeight.W500
    public val bold: FontWeight = FontWeight.W600
}
